function soma(n1, n2) {
  return n1 + n2;
}
console.log(soma(10, 4));

function multiplicacao(n1, n2) {
  return n1 * n2;
}
console.log(multiplicacao(4, 10));
function divisao(n1, n2) {
  return n1 / n2;
}
console.log(divisao(10, 4));
function subtracao(n1, n2) {
  return n1 - n2;
}
console.log(subtracao(10, 4));
